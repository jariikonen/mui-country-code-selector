/**
 * ISO 3166 two letter country codes of areas that have an ITU-T E.164 or NANP
 * telephone calling code.
 *
 * NOTES! Although Ascencion (SH-AC) is a subdivision of a larger entity, it
 * has its own calling code, so it is included. Bouvet Island (BV), French
 * Southern Territories (TF), and Heard Island and McDonald Islands (HM),
 * however, have ISO 3166 country codes, but they do not have their own calling
 * codes, so they are not included.
 *
 * See {@link https://stackoverflow.com/a/54061487} and
 * {@link https://github.com/Microsoft/TypeScript/pull/29510} for more
 * information about the type syntax.
 */
export type IsoAlpha2 = (typeof isoAlpha2Arr)[number];

/**
 * A short country name or an array of names for the country.
 */
export type CountryName = string | string[];

/**
 * A ITU-T E.164 assigned country calling code or an array of such calling
 * codes.
 */
export type CountryCode = string | string[];

/**
 * Represents a relationship between a calling code and an ISO 3166 two letter
 * country code.
 */
export type CountryCodeRecord = Record<IsoAlpha2, CountryCode>;

/**
 * Represents a relationship between a country short name and an ISO 3166 two
 * letter country code.
 */
export type CountryNameRecord = Record<IsoAlpha2, CountryName>;
